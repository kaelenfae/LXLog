export function getGelColor(input) {
    if (!input) return 'transparent';

    // Normalize input: remove spaces, uppercase
    const cleanInput = input.replace(/\s+/g, '').toUpperCase();

    // Check direct match
    if (GEL_DATA[cleanInput]) return GEL_DATA[cleanInput];

    // Attempt zero-padding for R/L codes
    // Matches Letter(s) followed by Number(s) e.g. R6, L2, L05
    const match = cleanInput.match(/^([A-Z]+)(\d+)$/);
    if (match) {
        const prefix = match[1];
        const number = match[2];

        // Try padding to 2 digits (e.g. R6 -> R06)
        const pad2 = prefix + number.padStart(2, '0');
        if (GEL_DATA[pad2]) return GEL_DATA[pad2];

        // Try padding to 3 digits (e.g. L2 -> L002)
        const pad3 = prefix + number.padStart(3, '0');
        if (GEL_DATA[pad3]) return GEL_DATA[pad3];
    }

    // If input is a valid hex code, return it
    if (/^#[0-9A-F]{6}$/i.test(cleanInput) || /^#[0-9A-F]{3}$/i.test(cleanInput)) {
        return cleanInput;
    }

    return 'transparent';
}

export function getContrastColor(hexColor) {
    if (!hexColor || hexColor === 'transparent') return 'inherit';

    // Convert hex to RGB
    const r = parseInt(hexColor.substr(1, 2), 16);
    const g = parseInt(hexColor.substr(3, 2), 16);
    const b = parseInt(hexColor.substr(5, 2), 16);

    // Calculate brightness (YIQ)
    const yiq = ((r * 299) + (g * 587) + (b * 114)) / 1000;

    return (yiq >= 128) ? '#000000' : '#FFFFFF';
}
